package org.tnsif.introduction;

public class DataTypeDemo {
	

	public static void main(String[] args) {

		int num1 = 5/2;
		
		float num2 = 103/45;
		float num3 = 103f/45f;    //f is used to give 7 nos. after the decimal //
		
		double num4 = 15/3;
		double num5 = 10d/3d;   //d is used to give 16 nos. after the decimal //
		
		System.out.println("The value is: " +num1);
		System.out.println("The value is: " +num2);
		System.out.println("The value is: " +num3);
		System.out.println("The value is: " +num4);
		System.out.println("The value is: " +num5);

}
}
